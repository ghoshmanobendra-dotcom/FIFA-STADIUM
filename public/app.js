function initAnnounce() {
  onSubmit('#announce-form', '#announce-output', async (out) => {
    const languages = $$('#announce-langs .chip[aria-pressed="true"]').map((c) => c.dataset.lang);
    const result = await api('/announce', {
      method: 'POST',
      body: JSON.stringify({
        scenario: $('#announce-scenario').value,
        message: $('#announce-message').value || undefined,
        languages: languages.length ? languages : undefined,
      }),
    });
    const items = result.languages
      .map(
        (l) =>
          `<div class="ann-item" lang="${l.language}"${RTL.has(l.language) ? ' dir="rtl"' : ''}>` +
          `<div class="lang">${LANG_LABELS[l.language] || l.language}</div>${escape(l.text)}</div>`,
      )
      .join('');
    showResult(
      out,
      `<h3>Announcement (${escape(result.scenario)})</h3>${items}${sourceBadge('offline')}`,
    );
  });
}

function initGreen() {
  onSubmit('#green-form', '#green-output', async (out) => {
    const result = await api('/sustainability/footprint', {
      method: 'POST',
      body: JSON.stringify({
        distanceKm: Number($('#green-distance').value),
        partySize: Number($('#green-party').value),
      }),
    });
    const max = Math.max(...result.options.map((o) => o.totalKgCO2e), 0.01);
    const rows = result.options
      .map((o) => {
        const best = o.mode === result.greenest ? ' best' : '';
        const pct = Math.max(2, Math.round((o.totalKgCO2e / max) * 100));
        return (
          `<li class="${best}"><span class="rname">${escape(o.label)}</span>` +
          `<span class="rbar"><span style="width:${pct}%"></span></span>` +
          `<span class="rval">${o.totalKgCO2e} kg</span></li>`
        );
      })
      .join('');
    showResult(
      out,
      `<h3>Greenest: ${escape(result.options[0].label)} · saves ${result.savingKg} kg (${result.savingPct}%)</h3>` +
        `<ul class="rank">${rows}</ul><p>${escape(result.advice)}</p>${sourceBadge(result.source)}`,
    );
  });
}

function initPlan() {
  onSubmit('#plan-form', '#plan-output', async (out) => {
    const result = await api(
      `/plan/${$('#plan-venue').value}?travelMinutes=${Number($('#plan-travel').value) || 45}`,
    );
    if (!result.match) {
      showResult(out, `<p>${escape(result.plan)}</p>`);
      return;
    }
    const fmt = (iso) =>
      new Date(iso).toLocaleString(undefined, { dateStyle: 'medium', timeStyle: 'short' });
    showResult(
      out,
      `<h3>${escape(result.match.stage)}: ${escape(result.match.home)} v ${escape(result.match.away)}</h3>` +
        `<ul class="timeline">` +
        `<li><span class="t">Leave by</span> — ${fmt(result.recommendedDepartBy)} (${result.travelMinutes} min trip)</li>` +
        `<li><span class="t">Arrive by</span> — ${fmt(result.recommendedArriveBy)} (gates open)</li>` +
        `<li><span class="t">Kickoff</span> — ${escape(result.match.kickoffLocal)}</li>` +
        `</ul><p>${escape(result.plan)}</p>${sourceBadge(result.source)}`,
    );
  });
}

function initTranslate() {
  onSubmit('#translate-form', '#translate-output', async (out) => {
    const target = $('#translate-target').value;
    const result = await api('/translate', {
      method: 'POST',
      body: JSON.stringify({ text: $('#translate-text').value, target }),
    });
    showResult(
      out,
      `<h3>Translation</h3><p>${escape(result.text)}</p>${sourceBadge(result.source)}`,
      {
        lang: result.target,
      },
    );
  });
}

